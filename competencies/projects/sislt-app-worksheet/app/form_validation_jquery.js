//DOMESTIC 1 ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
$(document).ready(function() {
 $('#d1_personal_info_form').validate({
   
   rules: {
     d1_emailAddress: {
        required: true,
        email: true
     },
	 d1_legalName: {
        required: true
     },  
	 d1_ApplyYourselfID: {
        required: true,
     }, 
	 d1_DateOfBirth: {
        required: true,
		date:true
     }, 
	 
     }, // end of rules 
	 
   messages: {
      d1_emailAddress: {
         required: "Please type an e-mail address.",
         email: "This is not a valid e-mail address."
       },
      d1_legalName: {
        required: 'Please type a name.'
      },
	  d1_ApplyYourselfID: {
        required: 'Please type a ApplyYourself ID.'
      },
	  d1_DateOfBirth: {
         required: "Please enter a date of birth.",
         date: "Please enter a date format of MM/DD/YYYY."
       }
	  
   }
  }); // end validate 
  
}); // end ready
